    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String sql1="select	b_amount from fuel_pump Where p_num=? and f_type=?";
            pst=conn.prepareStatement(sql1);
            pst.setString(1,(String)jComboBox1.getSelectedItem() );
            pst.setString(2, (String)jComboBox3.getSelectedItem());
            rs= pst.executeQuery();
            if(rs.next()){
                int amountPump=Integer.parseInt(rs.getString("b_amount"));
                int amountInput=Integer.parseInt(jTextField1.getText());
                if(amountInput>amountPump){
                    JOptionPane.showMessageDialog(null, "Sorry No Fuel Enough");
                    rs.close();
                    pst.close(); 
                    return;
                }
                if(amountInput==0 ||amountInput<0 ){
                    JOptionPane.showMessageDialog(null, "Sorry Enter Postive Nmuber Bigger Than Zero!!");
                    rs.close();
                    pst.close(); 
                    return;
                }
            }

            String sql="select liters_price from fuel Where f_type=?";
            pst=conn.prepareStatement(sql);
            pst.setString(1, (String)jComboBox3.getSelectedItem());
            rs= pst.executeQuery();
            if(rs.next()){
                int Quantity= Integer.parseInt(jTextField1.getText()); 
                int amount= Integer.parseInt(rs.getString("liters_price")) * Quantity; 
                jTextField2.setText(Integer.toString(amount));
                pst.close();  
                rs.close();
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Something Error, Please Try Later!");
        }   
    }//GEN-LAST:event_jButton1ActionPerformed

