    // Fill Pump Number
    public void fillJComboBox1() {
        String sql="select p_num from pump";
        try{
            pst=conn.prepareStatement(sql);
            rs= pst.executeQuery();
            jComboBox1.addItem("Select Pump ID");
            while(rs.next()){
                jComboBox1.addItem(rs.getString("p_num"));  
            }
            pst.close();  
            rs.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Something Wrong !!");
        }   
    }
    
    // Fill fuel type
    public void fillJComboBox3() {
        try{
            jComboBox3.removeAllItems();
            jComboBox3.addItem("Select Fuel Type");
            String sql="select DISTINCT f_type from fuel_pump where p_num=?";
            pst=conn.prepareStatement(sql);
            pst.setInt(1, Integer.parseInt(jComboBox1.getSelectedItem().toString()));
            rs= pst.executeQuery();
            while(rs.next()){
                jComboBox3.addItem(rs.getString("f_type"));  
            }
            pst.close();  
            rs.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Something Wrong !!");
        }   
    }
